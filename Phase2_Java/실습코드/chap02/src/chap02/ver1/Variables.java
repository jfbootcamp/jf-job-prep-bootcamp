package chap02.ver1;

public class Variables {

	public static void main(String[] args) {
		// 변수 선언과 초기화
		// 정수형 변수 age를 선언하고 25로 초기화하세요
		int age = 25;
		
		// 실수형 변수 height를 선언하고 178.5로 초기화하세요.
		double height = 178.5;
		
		System.out.println("나이: " + age);
		System.out.println("키: " + height);
	}
}
